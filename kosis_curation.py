    """자연어 라우팅 결과.

    route_type:
      - direct: Tier A 직접조회 가능
      - search: 검색 후보 제공
      - hybrid: 직접조회와 검색 후보 모두 있음
      - miss: 매핑 없음
    """

    query: str
    normalized: str
    route_type: str
    direct_stat_key: Optional[str] = None
    direct_stat: Optional[QuickStatParam] = None
    search_terms: list[str] = field(default_factory=list)
    matched_concepts: list[str] = field(default_factory=list)
